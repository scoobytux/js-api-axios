function User(
  _account,
  _fullName,
  _password,
  _email,
  _image,
  _userType,
  _language,
  _description
) {
  this.account = _account;
  this.fullName = _fullName;
  this.password = _password;
  this.email = _email;
  this.image = _image;
  this.userType = _userType;
  this.language = _language;
  this.description = _description;
}
